import mongoose from 'mongoose';

const cartItemSchema = new mongoose.Schema({
  productId: {
    type: Number,
    required: true
  },
  title: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  },
  image: {
    type: String,
    required: true
  },
  quantity: {
    type: Number,
    required: true,
    min: 1,
    default: 1
  }
}, {
  timestamps: true
});

const Cart = mongoose.model('Cart', cartItemSchema);

export default Cart;

